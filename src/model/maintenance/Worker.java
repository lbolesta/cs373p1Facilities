package model.maintenance;

public class Worker {
	private String name;
	private String wage;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getWage() {
		return wage;
	}
	
	public void setWage(String wage) {
		this.wage = wage;
	}
}
