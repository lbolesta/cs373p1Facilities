package main.model.maintenance;

public class Worker {
	private String name;
	private float wage;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public float getWage() {
		return wage;
	}
	
	public void setWage(float wage) {
		this.wage = wage;
	}
}
